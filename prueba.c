int main(){
	HashTable h;
	initHashTable(&h,7);

	printTable(&h);

	addToTable(&h, "Jorge", 56);
	addToTable(&h, "Jose", 101);
	addToTable(&h, "Pablo", 5);
	addToTable(&h, "Graciela", 6);

	printTable(&h);

	removeFromTable(&h, "Jorge");
	removeFromTable(&h, "Pablo");
	removeFromTable(&h, "Maria");

	printTable(&h);

	finishHashTable(&h);
	return 0;
}
